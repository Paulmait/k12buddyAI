// Consent version - increment this when policies change to re-prompt users
const CONSENT_VERSION = '1.0';
const CONSENT_STORAGE_KEY = 'k12buddy_ai_consent';

interface ConsentRecord {
  version: string;
  accepted_at: string;
  allow_child_ai: boolean;
}

export async function getConsentStatus(): Promise<ConsentRecord | null> {
  try {
    const stored = await AsyncStorage.getItem(CONSENT_STORAGE_KEY);
    if (!stored) return null;

    const consent: ConsentRecord = JSON.parse(stored);

    // Check if consent version matches current version
    if (consent.version !== CONSENT_VERSION) {
      return null; // Re-prompt for new version
    }

    return consent;
  } catch {
    return null;
  }
}

export async function hasValidConsent(): Promise<boolean> {
  const consent = await getConsentStatus();
  return consent !== null;
}

